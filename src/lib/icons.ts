// Get display name from icon (e.g., 'mdi:floor-lamp' -> 'Floor lamp')
export function getIconDisplayName(icon: string): string {
  const name = icon.replace('mdi:', '').replace(/-/g, ' ')
  return name.charAt(0).toUpperCase() + name.slice(1)
}

// Search icons by query
export function searchIcons(query: string): string[] {
  if (!query.trim()) return COMMON_ICONS

  const lowerQuery = query.toLowerCase()

  return COMMON_ICONS.filter((icon) => {
    // Match icon name
    const iconName = icon.replace('mdi:', '').replace(/-/g, ' ')
    if (iconName.includes(lowerQuery)) return true

    // Match keywords
    const keywords = ICON_KEYWORDS[icon]
    if (keywords?.some((kw) => kw.includes(lowerQuery))) return true

    return false
  })
}
